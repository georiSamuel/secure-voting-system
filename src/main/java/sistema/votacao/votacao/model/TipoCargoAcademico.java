package sistema.votacao.votacao.model;

/**
 * Enumeração que representa os diferentes tipos de cargos acadêmicos
 * disponíveis em uma votação institucional, como reitorias e coordenações.
 * Utilizada para definir o cargo em disputa nas votações acadêmicas.
 * 
 * @author Lethycia
 * @version 1.0
 * @since 26/05/25
 */
public enum TipoCargoAcademico {
    REITOR,
    VICE_REITOR,
    DIRETOR,
    DIRETOR_ADJUNTO,
    COORDENADOR,
    COORDENADOR_ADJUNTO
}
